using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Pg111_Assignment
{
    public class Math
    {
        public int Add(int x, int plus)
        {
            return x + plus;
        }

        public int Sub(int y, int minus)
        {
            return y - minus;
        }

        public int Mult(int z, int multi)
        {
            return z * multi;
        }
    }
}
